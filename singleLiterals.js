// 1. Single and Double Quoted Strings
// Single and double quotes are functionally the same. The key difference is that you can nest one type of quotes inside the other without escaping them.

let singleQuoteString = 'This is a string in single quotes';
let doubleQuoteString = "This is a string in double quotes";

// Nesting quotes
let nestedQuotes = 'She said, "Hello!"';  // Double quotes inside single quotes
let nestedQuotes2 = "It's a beautiful day";  // Single quote inside double quotes

